public class Escape
{
    public static void main(String[] args) {
        System.out.println("hello \"Java Programming\"");

        System.out.println("\"hello \"Java\nProgramming\"");
        System.out.println("\"hello \"Java\\nProgramming\"");
        System.out.println("\"hello \"Java\tProgramming\"");
        System.out.println("\"hello \"Java\bProgramming\"");

    }
}
